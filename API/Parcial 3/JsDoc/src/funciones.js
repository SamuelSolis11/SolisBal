 /**
  *  Esta funcion recibe un numero que elevar y lo va a regresar ya elevado a la potencia que es.
  * @param {int} numerobase  Numero que se va elevar
  * @param {int} exponente Potencia a la que se va elevar el numero
  * 
  */
 export function potencia(numerobase,exponente){
    return numerobase*exponente
}